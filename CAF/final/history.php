		<h2>CAF moves to Cairo</h2>
		<p>The young organisation suffered an early setback when its important documents were lost during a fire outbreak at the offices of the Sudan Football Association where they were kept. Lucky it was that the statutes adopted at the Constitutional Assembly had already been sent to FIFA and they were subsequently approved by the FIFA Executive Committee on June 21, 1957, in Zurich.Thus going by Article 5 of these statutes which stipulates that the headquarters of CAF be located in the same country as where the president resides, the organisation's first office was on 3, El Hadika Street, Garden City, Cairo.</p>
</section>
