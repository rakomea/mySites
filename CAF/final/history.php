	At the next General Assembly in Accra, on November 22, 1963, a decision was taken to create a new competition for clubs the African Cup of Champions Clubs.A year later, during the Congress in Tokyo, on the eve of the 1964 Summer Olympics, a counter proposal co-sponsored by Ethiopia, Egypt and then new member Ghana, calling for the expulsion of Apartheid South Africa, surprisingly won the majority vote for the reduced, but equally effective suspension of Apartheid, reversing the 1963 decision. Ceaseless efforts by Sir Stanley to change this new status were successfully blocked by Africa. As many as 25 African countries were represented during this congress
