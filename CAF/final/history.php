	<h2 >1954-1957</h2>
			<p>The Confederation of African Football began its formation at the 29th FIFA Congress in Bern, Switzerland, in June 1954. Africa was represented at the Congress by four national associations: Egypt, who had joined FIFA in 1923; Sudan affiliated in 1948; Ethiopia in 1953 and South Africa in 1910 and 1952. It was at that Congress that Africa was recognised as a zonal group with a right to have for the first time a representative on the FIFA Executive Committee. This representation was taken up by Engineer Abdel Aziz Abdallah Salem of Egypt.The foundation of the CAF statutes came from proposed statutes by both Tessema and Shaddad while regulations were also drawn for the African Cup of Nations</p>
