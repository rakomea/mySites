						<p> 
							<strong>Calvin Cordozar Broadus Jr.</strong>  (born October 20, 1971), known professionally as Snoop Dogg is an American rapper, singer, record producer, television personality, entrepreneur, and actor. His music career began in 1992 when he was discovered by Dr. Dre and featured on Dre's solo debut, "Deep Cover", and then on Dre's solo debut album, The Chronic. He has since sold over 23 million albums in the United States and 35 million albums worldwide.</p>
						<p>
							 Snoop's debut album, Doggystyle, produced by Dr. Dre, was released in 1993 by Death Row Records. Bolstered by excitement driven by Snoop's featuring on The Chronic,the album debuted at number one on both the Billboard 200 and Billboard Top R&B/Hip-Hop Albums charts. Selling almost a million copies in the first week of its release, Doggystyle became certified quadruple platinum in 1994 and spawned several hit singles, including "What's My Name?" and "Gin & Juice". In 1994 Snoop released a soundtrack on Death Row Records for the short film Murder Was the Case, starring himself. His second album, Tha Doggfather (1996), also debuted at number one on both charts, with "Snoop's Upside Ya Head" as the lead single. The album was certified double platinum in 1997. 
						</p>
						<p>
							After leaving Death Row Records, Snoop signed with No Limit Records, where he recorded his next three albums, Da Game Is to Be Sold, Not to Be Told (1998), No Limit Top Dogg (1999), and Tha Last Meal (2000). Snoop then signed with Priority/Capitol/EMI Records in 2002, where he released Paid tha Cost to Be da Boss. He then signed with Geffen Records in 2004 for his next three albums, R&G (Rhythm & Gangsta): The Masterpiece, Tha Blue Carpet Treatment, and Ego Trippin'. Malice 'n Wonderland (2009), and Doggumentary (2011) were released on Priority. Snoop Dogg has starred in motion pictures and hosted several television shows, including Doggy Fizzle Televizzle, Snoop Dogg's Father Hood, and Dogg After Dark. He also coaches a youth football league and high school football team. In September 2009 Snoop was hired by EMI as the chairman of a reactivated Priority Records. 
						</p>
