		<h2>International development leadership and non-profit work</h2>

		<p>Okonjo-Iweala is Chair of the Board of the African Union's African Risk Capacity, an innovative weather based insurance mechanism for African countries. She is also Chair of the Board of the Nelson Mandela Institution, an umbrella body for the African Institutes of Science and Technology, and Chair of the Board of the African University of Science and Technology in Nigeria. In addition, she is a member of numerous boards and advisory groups including the Harvard University Advisory Council, the University of Oxford's Martin Schools Advisory Council, the Asian Infrastructure Investment Bank's International Advisory Panel, the International Commission on Financing Global Education (Chaired by Gordon Brown), the Japan International Cooperation Agency's International Advisory Board, the Mercy Corps Global Leadership Council, Womens World Banking, Results for Development Institute, the B Team (Co-chaired by Sir Richard Branson), the Commission on the New Climate Economy (also co-Chaired by Mr Paul Polman and Lord Nicholas Stern) and the Global Development Network amongst others.
		</p>

		<p>Previously, she served as the co-Chair of the Global Partnership for Effective Development Cooperation and Chair of the World Banks Development Committee (2004).
		</p>
		<p>She was also a board member of the Rockefeller Foundation Board, the One Campaign, the World Economic Forum Young Global Leaders, the University of Oxfords Blavatnik School of Governments International Advisory Board, the World Economic Forum Young Global Leaders Foundation, the International Monetary and Finance Committee of the IMF (2003-2006 and 2011-2015), the United Nations Secretary Generals High-Level Panel on the Post-2015 Development Agenda, the Danish-Government-led Commission on Africa, the World Economic Forum Global Leadership Council on Transparency and Corruption, and the renowned Commission on World Growth, led by Nobel Prize winner Professor Michael Spence.
		</p>
		<p>Okonjo-Iweala is the founder of Nigerias first indigenous opinion-research organization, NOI-Polls. She also founded the Center for the Study of Economies of Africa (C-SEA), a development research think tank based in Abuja, Nigerias capital and is a Distinguished Visiting Fellow at the Center for Global Development and the Brookings Institution.
		</p>
		<p>In 2012, Okonjo-Iweala was a candidate for president of the <a href ="https://www.worldbank.org/">World Bank</a>, running against Dartmouth College President Jim Yong Kim. If elected, Okonjo-Iweala would have been the first female president of the World Bank.
		</p>
