		<h2>Career</h2>
		<figure class="page-image">
		<img class="flexible" src="images/okonjo2.jpg" alt="Ngozi">
		<figcaption class="captions">Ngozi Okonjo-Iweala</figcaption>
		</figure>
		<p>Okonjo-Iweala spent a 25-year career at the <a href ="https://www.worldbank.org/">World Bank </a> in Washington DC as a development economist, rising to the No. 2 position of Managing Director. As Managing Director, she had oversight responsibility for the <a href ="https://www.worldbank.org/">World Bank</a> $81 billion operational portfolio in Africa, South Asia, Europe and Central Asia.[3] Okonjo-Iweala spearheaded several World Bank initiatives to assist low-income countries during the 2008 “ 2009 food crises and later during the financial crisis. In 2010, she was Chair of the IDA replenishment, the <a href ="https://www.worldbank.org/">World Bank</a> successful drive to raise $49.3 billion in grants and low interest credit for the poorest countries in the world. During her time at the <a href ="https://www.worldbank.org/">World Bank</a>, she was also a member of the Commission on Effective Development Cooperation with Africa
		</p>

		<p>Okonjo-Iweala served twice as Nigeria's Finance Minister and also as Minister of Foreign Affairs. She was the first female to hold both positions. During her first term as Minister of Finance under president Obasanjos Administration, she spearheaded negotiations with the Paris Club of Creditors that led to the wiping out of US$30 billion of Nigeria's debt, including the outright cancellation of US$18 billion.In 2003 she led efforts to improve Nigeria's macroeconomic management including the implementation of an oil-price based fiscal rule where revenues accruing above a reference benchmark oil price were saved in a special account,The Excess Crude Account which helped to reduce macroeconomic volatility.
		</p>
		<div>Following her first term as Minister of Finance, she returned to the <a href ="https://www.worldbank.org/">World Bank </a> as a Managing Director in December 2007.
		</div>
		<p>In 2011, Okonjo-Iweala was reappointed as Minister of Finance in Nigeria with the expanded portfolio of the Coordinating Minister for the Economy by PresidentÂ Goodluck Jonathan. Her legacy includes strengthening Nigeria's public financial systems and stimulating the housing sector with the establishment of the Nigerian Mortgage Refinance Corporation (NMRC). She also empowered Nigeria's women and youth with the Growing Girls and Women in Nigeria Programme (GWIN); a gender responsive budgeting system, and the highly acclaimed Youth Enterprise with Innovation programme (YouWIN); to support entrepreneurs, that created thousands of jobs. This program has been evaluated by the World Bank as one of the most effective programmes of its kind globally.
		</p>

		<p> Under her leadership, the National Bureau of Statistics carried out a rebasing exercise of the Gross Domestic Product (GDP); the first in 24 years, which saw Nigeria emerge as the largest economy in Africa. She took a lot of heat for the fuel subsidy removal policy by the Nigerian government, which led to protests in January 2012.In May 2016, the new Nigerian administration eventually removed the fuel subsidy after it became apparent that it was unsustainable and inefficient.	
		</p>

		<p>In September 2015, she joined Lazard as a Senior Advisor and in January 2016 she was appointed Chair of the <a href="https://www.gavi.org/">Global Alliance for Vaccines and Immunization (GAVI)</a>.</p>
		<p>She is co-chair of the Global Commission for the Economy and Climate, with Nicholas Stern and Paul Polman.In July 2017, she became an independent non-executive director at Standard Chartered PLC. 2017</p>
		<p>Jack Dorsey announced on July 19, 2018 that Okonjo-Iweala would join Twitter's Board of Directors.</p>
