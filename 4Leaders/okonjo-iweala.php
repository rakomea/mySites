		<h2>Who is that?</h2>
		<p>Ngozi Okonjo-Iweala (born 13 June 1954) is an economist and international development expert. She sits on the Boards of <a href ="https://www.sc.com/en/">Standard Chartered Bank</a>, <a href ="https://twitter.com/?lang=en" >Twitter</a>, <a href="https://www.gavi.org/">Global Alliance for Vaccines and Immunization (GAVI)</a>, and the African Risk Capacity (ARC).
		</p>
	
		<h2>Education and Personal Life</h2>
		<figure class="page-image">
		<img class="flexible" src="images/Okonjo1.jpg" alt="Ngozi">
		<figcaption class="captions">Say Cheese!Selfie with her husband</figcaption>
		</figure>
		<p> Okonjo-Iweala was born in Ogwashi-Ukwu, Delta State, <a href ="https://en.wikipedia.org/wiki/Nigeria">Nigeria </a> where her father Professor Chukwuka Okonjo is the Eze (King) from the Obahai Royal Family of Ogwashi-Ukwu.
		</p>
			
		<p> Okonjo-Iweala was educated at Queen's School, Enugu, St. Anne's School, Molete, Ibadan, and the International School Ibadan. She arrived in the USA in 1973 as a teenager to study at Harvard University, graduating magna cum laude with an AB in Economics in 1976. In 1981, she earned her PhD in regional economics and development from the Massachusetts Institute of Technology (MIT) with a thesis titled Credit policy, rural financial markets, and Nigeria's agricultural development.She received an International Fellowship from the <a href ="https://www.aauw.org/">American Association of University Women (AAUW)</a> that supported her doctoral studies.
		</p>			
		<p> She is married to Dr. Ikemba Iweala, a neurosurgeon. They have four children - Onyinye Iweala (AB, MD, PhD, Harvard), Uzodinma Iweala (AB, Harvard, MD, Columbia) , Okechukwu Iweala (AB, Harvard) and Uchechi Iweala (AB, MD, MBA, Harvard).
		</p>
